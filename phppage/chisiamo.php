            Data la grande affluenza di sciatori negli impianti, nel 1990 si è deciso di aumentare il numero di piste alle attuali 31.
            In seguito alla sempre maggiore affluenza di sciatori e turisti si sta pianificando l'estensione della rete di piste verso Vallone Dolonne.</p>
        
