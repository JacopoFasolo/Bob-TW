            <p>La costruzione degli impianti è iniziata nel 1934.
            Inizialmente la prima costruzione prevedeva solo una funivia con arrivo a <span xml:lang="FR">Col Checrouit</span>(2256mt) e 12 piste da sci che si diramano lungo il crinale per raggiungere <span xml:lang="FR">Val Checrouit</span>(2256mt).
